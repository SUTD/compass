#ifndef TYPES_H_
#define TYPES_H_

/**
 * This file defines fixed-size types to be used if the sixe is actually
 * critical.
 */

/*
 * A 64 bit int
 */
typedef long int64;
typedef unsigned long uint64;

/*
 * A 32 bit int
 */
typedef int int32;
typedef unsigned int uint32;

/*
 * A 16 bit int
 */
typedef short int16;
typedef unsigned short uint16;

#endif /*TYPES_H_*/
